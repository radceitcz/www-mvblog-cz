        <h2>Jednotlivé kryptoměny</h2>
        <h3>Bitcoin (BTC)</h3>
        <p>Bitcoin (BTC) je decentralizovaná kryptoměna, kterou v roce 2008 poprvé popsal jednotlivec nebo skupina jednotlivců pod přezdívkou Satoshi Nakamoto. Bitcoin, který byl oficiálně spuštěn v lednu 2009, je online měna typu peer-to-peer (P2P), která umožňuje uskutečňovat transakce přímo mezi rovnocennými a nezávislými účastníky sítě bez potřeby jakéhokoli prostředníka. Bitcoin jsou digitální peníze, které nemůže nafouknout ani zmanipulovat žádný jednotlivec, společnost, vláda ani centrální banka. Bitcoin je uznáván jako jedna z prvních kryptoměn, které se začaly používat, a inspiroval vývoj tisíců konkurenčních projektů. Vždy bude existovat pouze 21 milionů BTC. Bitcoin je vysoce dělitelný, přičemž jeho nejmenší jednotka, tj. 0,000 000 01 BTC, se nazývá "satoshi" nebo "sat". S rostoucí hodnotou bitcoinu se jeho snadná dělitelnost stala klíčovým atributem.</p>
        <h3>Litecoin (LTC)</h3>
        <p>Litecoin je peer-to-peer (P2P) kryptoměna která umožňuje okamžité platby s téměř nulovými náklady komukoli na světě. Litecoin je globální platební síť s otevřeným zdrojovým kódem, která je plně decentralizovaná bez centrálních orgánů. Matematika zabezpečuje síť a dává jednotlivcům možnost kontrolovat své vlastní finance. Litecoin se vyznačuje rychlejšími časy potvrzení transakcí a lepší efektivitou ukládání než přední měny založené na matematice. Díky značné podpoře průmyslu, objemu obchodů a likviditě je Litecoin osvědčeným obchodním prostředkem doplňujícím Bitcoin.</p>
        
